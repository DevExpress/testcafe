/*eslint-disable no-console*/
function triggerConsoleMessages () { //eslint-disable-line @typescript-eslint/no-unused-vars
    console.log('log-' + document.title);
    console.warn('warn-' + document.title);
    console.error('error-' + document.title);
    console.info('info-' + document.title);
}
/*eslint-enable no-console*/
